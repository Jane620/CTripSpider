package com.jxn.ctrip.entity;

public class SimRestaurant {

	private int id;
	
	private double lat;
	
	private double lng;
	
	private double bd_lat;
	
	private double bd_lng;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}

	public double getBd_lat() {
		return bd_lat;
	}

	public void setBd_lat(double bd_lat) {
		this.bd_lat = bd_lat;
	}

	public double getBd_lng() {
		return bd_lng;
	}

	public void setBd_lng(double bd_lng) {
		this.bd_lng = bd_lng;
	}
	
}
